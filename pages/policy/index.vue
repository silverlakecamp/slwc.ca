          <h2 id="head-lice">Head Lice</h2>

          <br>

          <p>At Silver Lake Wesleyan Camp we strive to maintain a consistently healthy environment for our campers. As a community living in close quarters, head lice can spread quickly and be hard to eliminate. In an effort to prevent lice, we ask parents to check their child’s head before they come to camp. In order to assist parents and maintain a healthy camp we abide by the following policy:</p>

          <ul>
            <li>Upon check-in on the first day of camp each camper will be checked for head lice.</li>
            <li>If any lice or nits are found at that time or at any time during the camping session, the camper will be asked to return home.</li>
            <li>
              Once the camper is lice and nit free, they will have the following options:
              <ul>
                <li>Choose another week – Parents can bring their child back for another week of equal value during the current summer, if space is available.</li>
                <li>In the event another week is not available you may choose a refund (two-thirds of the fee)</li>
                <li>Apply 2/3 of the current year fee towards your child’s camp the following season.</li>
              </ul>
            </li>
          </ul>

