          <h2 id="refunds">Refunds</h2>

          <br>

          <p>Here at SLWC we understand that life happens, plans change and circumstances beyond your control may necessitate the cancellation of your camp registration. We also realize that budgets are built, staff hired and supplies purchased based on registrations, all of which are impacted by cancellations. So that there are no surprises or misunderstandings here is what happens in the case of cancellation.</p>

          <h3>Full Refund</h3>

          <br>

          <ul>
            <li>If your selected camp session(s) is full and no alternative can be found or if the financial aid amount requested is not fully granted, the total amount paid to the camp will be refunded (less admin fee of $12.00)</li>
          </ul>

          <br>

          <h3>Partial Refund</h3>

          <br>

          <p>The balance of the camp fee (less admin fee of $25.00) will be refunded IF:</p>

          <ul>
            <li>Family plans change and the camper can no longer attend. In this case written notification must be received according to the timeframe on the chart below.</li>
            <li>The camper does not attend due to illness or injury. A letter from a physician must accompany the refund request.</li>
            <li>The camper is sent home from camp due to illness or injury occurring at camp.</li>
            <li>Refunds are prorated in such cases and will not be made for less than half the camp session and will be made only at the written request of the parent/guardian.</li>
            <li>No refunds will be made if the camper does not attend his/her camp program or leaves early for any other reason, even if the space is filled by another participant.</li>
          </ul>

          <br>

          <p> We reserve the right to grant exceptions to the stated policy in situations including, but not limited to, illness and death in the immediate family.</p>

          <h3>Time of Request Refund</h3>

          <br>

          <ul>
            <li>At least 15 days before start of the camp <b>100%</b></li>
            <li>7 to 14 days before start of the camp <b>50%</b></li>
            <li>Less than 7 days before start of the camp <b>25%</b></li>
            <li>After the start of the camp <b>No Refund</b></li>
