                        campers were registered for 2022 (just over 1200.) Equally stunning was the growth of the number
                        of rental groups that use our facility. From about ½ dozen in the early 2000’s, to a record 27
                        in 2016. We are currently on track to best that number with 38 groups and events registered for
                        2024.
